class User extends Authenticatable implements JWTSubject
{
    use Notifiable;
    use HasRoles;
    use ActiveUserHelper;
    use LastActiveHelper;
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name', 'phone', 'email', 'password', 'introduction', 'avatar',
        'weixin_openid', 'weixin_unionid', 'registration_id',
        'weixin_session_key', 'weapp_openid',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password', 'remember_token',
    ];

    public function topices(){
        return $this->hasMany(Topic::class);
    }

    public function replies()
    {
        return $this->hasMany(Reply::class);
    }

    public function markAsRead(){
        $this->notification_count = 0;
        $this->save();
        $this->unreadNotifications->markAsRead();
    }

    public function setPasswordAttribute($value){
        if(strlen($value) != 60){
            $value = \Hash::make($value);
        }
        $this->attributes["password"] = $value;
    }

    public function setAvatarAttribute($path){
        if(!starts_with($path,"http")){
            $path = config("app.url")."/uploads/imgaes/avatars/".$path;
        }
        $this->attributes["avatar"] = $path;
    }

    /**
     * Get the identifier that will be stored in the subject claim of the JWT.
     *
     * @return mixed
     */
    public function getJWTIdentifier()
    {
        // TODO: Implement getJWTIdentifier() method.
        return $this->getKey();
    }

    /**
     * Return a key value array, containing any custom claims to be added to the JWT.
     *
     * @return array
     */
    public function getJWTCustomClaims()
    {
        // TODO: Implement getJWTCustomClaims() method.
        return [];
    }

    public function findForPassport($username){
        filter_var($username,FILTER_VALIDATE_EMAIL)?
            $credentails["email"] = $username:
            $credentails["phone"] = $username;
        return self::where($credentails)->first();
    }
}
